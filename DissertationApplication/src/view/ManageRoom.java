    private void btnUpdateRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateRoomActionPerformed
        // TODO add your handling code here:
        int bedNo;
        double roomPrice;
        String floorNo;
        String roomDesc;
        boolean ensuite;
        
        bedNo = Integer.parseInt(txtBedNumber.getText());
        roomPrice = Double.parseDouble(txtRoomPrice.getText());
        floorNo = txtFloorNumber.getText();
        roomDesc = txtDescription.getText();
        ensuite = radEnsuite.isSelected();
        
        RoomController rc = new RoomController();
        rc.updateRoom(roomNumber,bedNo, roomPrice, floorNo, roomDesc, ensuite);    
        JOptionPane.showMessageDialog(null, "Room Updated");
    }//GEN-LAST:event_btnUpdateRoomActionPerformed


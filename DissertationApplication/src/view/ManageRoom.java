    private void loadRoom() {
        RoomController rc = new RoomController();
        Object[] room = rc.loadRoom(roomNumber);
        
        String bedNo;
        String roomPrice;
        String floorNo;
        String roomDesc;
        boolean ensuite;
        
        floorNo = room[1].toString();
        bedNo = room[2].toString();
        roomDesc = room[3].toString();
        roomPrice = room[4].toString();
        ensuite = Boolean.parseBoolean(room[5].toString());
        
        txtRoomNumber.setText(roomNumber);
        txtFloorNumber.setText(floorNo);
        txtBedNumber.setText(bedNo);
        txtDescription.setText(roomDesc);
        txtRoomPrice.setText(roomPrice);
        radEnsuite.setSelected(ensuite);        
    }
